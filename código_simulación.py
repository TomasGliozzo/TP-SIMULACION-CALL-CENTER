# --- 6.1 CONFIGURACIÓN DEL MODELO Y HELPERS ---

import random
import math
import numpy as np

print("Cargando generadores y reglas de negocio con corrección temporal...")

# ---------------------------------------------------------
# 1. GENERADORES DE TIEMPO (FDPs Ajustadas)
# ---------------------------------------------------------
# CORRECCIÓN: (valor_ms / 1000) * 31 días para obtener el IA real en segundos por día

def generar_ia_madrugada():
    val_ms = max(0, random.lognormvariate(math.log(1687.3469), 1.4185) - 30.7713)
    return (val_ms / 1000.0) * 31.0

def generar_ia_maniana():
    val_ms = random.weibullvariate(290.6916, 0.8947)
    return (val_ms / 1000.0) * 31.0

def generar_ia_tarde():
    val_ms = random.gammavariate(1.0301, 209.7228)
    return (val_ms / 1000.0) * 31.0

def generar_ia_noche():
    val_ms = random.weibullvariate(291.9040, 0.9699)
    return (val_ms / 1000.0) * 31.0

# Los TA ya estaban en segundos, se mantienen intactos
def generar_ta_tecnica_internet(): return max(0, random.lognormvariate(math.log(4313.9606), 0.5637) - 751.2319)
def generar_ta_tecnica_catv():     return max(0, random.lognormvariate(math.log(3718.3236), 0.5292) - 701.4885)
def generar_ta_tecnica_flow():     return max(0, random.lognormvariate(math.log(4566.4092), 0.5607) - 807.2023)
def generar_ta_agenda():           return max(0, random.lognormvariate(math.log(2792.0761), 0.5983) - 312.2966)
def generar_ta_comercial():        return max(0, random.weibullvariate(2915.8499, 1.0465) + 47.9759)

# ---------------------------------------------------------
# 2. HELPERS DE LÓGICA DE NEGOCIO
# ---------------------------------------------------------
def obtener_tipo_consulta():
    r = random.random()
    if r <= 0.52: return "TECNICA INTERNET"
    elif r <= 0.74: return "TECNICA FLOW"
    elif r <= 0.83: return "ACTIVIDAD COMERCIAL"
    elif r <= 0.92: return "TECNICA CATV"
    else: return "AGENDA"

def obtener_tiempo_base(tipo_consulta):
    if tipo_consulta == "TECNICA INTERNET": return generar_ta_tecnica_internet()
    elif tipo_consulta == "TECNICA CATV": return generar_ta_tecnica_catv()
    elif tipo_consulta == "TECNICA FLOW": return generar_ta_tecnica_flow()
    elif tipo_consulta == "AGENDA": return generar_ta_agenda()
    elif tipo_consulta == "ACTIVIDAD COMERCIAL": return generar_ta_comercial()
    return 0

def evaluar_arrepentimiento(llamadas_en_espera):
    prob_abandono = 0.0
    if llamadas_en_espera <= 1: prob_abandono = 0.00
    elif llamadas_en_espera <= 15: prob_abandono = 0.08
    elif llamadas_en_espera <= 40: prob_abandono = 0.34
    else: prob_abandono = 0.66
    return random.random() < prob_abandono

def calcular_tiempo_junior(tiempo_base):
    penalizacion = random.uniform(0.15, 0.30)
    return tiempo_base * (1 + penalizacion)

def procesar_logica_ia(tiempo_base, es_madrugada):
    mejora_ia = random.triangular(0.10, 0.25, 0.30)
    tiempo_ia = tiempo_base * (1 - mejora_ia)
    r = random.random()
    if r < 0.65: estado = 'RESUELTO'
    elif r < 0.75: estado = 'FALLO' if es_madrugada else 'DERIVADO'
    else: estado = 'FALLO'
    return estado, tiempo_ia

