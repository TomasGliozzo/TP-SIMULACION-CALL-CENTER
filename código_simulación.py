    limite_superior = df_turno['interarribo_ms'].quantile(percentil)

    # c. Aplicamos el filtro
    df_filtrado = df_turno[df_turno['interarribo_ms'] <= limite_superior].copy()

    print(f"  > Registros originales: {len(df_turno)} | Filtrados (p{int(percentil*100)}): {len(df_filtrado)}")
    print(f"  > Corte de outlier: > {limite_superior:.2f} ms")

    return df_filtrado

