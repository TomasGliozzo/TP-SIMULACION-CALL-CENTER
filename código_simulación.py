def procesar_turno_filtrado(df_turno, percentil=0.99):
    # a. Ordenar y calcular interarribos
    df_turno = df_turno.sort_values('arribo_ms').copy()
    df_turno['interarribo_ms'] = df_turno['arribo_ms'].diff()
    df_turno['interarribo_ms'] = df_turno['interarribo_ms'].fillna(df_turno['arribo_ms'])

    # b. FILTRADO: Calculamos el límite superior (Percentil 99)
