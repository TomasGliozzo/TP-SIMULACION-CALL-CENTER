import random

def simular_turno(nombre_turno, NSR, NJR, NIA, fn_generar_ia, cursos_activos=False):
    HV = float('inf')
    T = 0.0
    TF = 21600 * 365

    TPLL = 0.0
    TPSO = [HV] * (NSR + NJR) if (NSR + NJR) > 0 else []
    TPSIA = [HV] * NIA if NIA > 0 else []
    ESTADO_IA = [''] * NIA

    # --- NUEVA LÓGICA: VECTOR DE MEJORA POR CAPACITACIÓN ---
    MEJORA_OP = [0.0] * (NSR + NJR)
    if cursos_activos:
        for i in range(NSR + NJR):
            if i < NJR:
                # Junior: Mejora entre 2% y 10% (Moda 6%)
                MEJORA_OP[i] = random.triangular(0.02, 0.06, 0.10)
            else:
                # Senior: Mejora entre 1% y 5% (Moda 3%)
                MEJORA_OP[i] = random.triangular(0.01, 0.03, 0.05)

    NS = 0; NSP = 0; NSO = 0; NSIA = 0
    NTSr = 0; NTJr = 0; NTIA_A = 0; NTIA_D = 0; NTIA_F = 0; ARRE = 0

    ITOO = [0.0] * (NSR + NJR)
    STOO = [0.0] * (NSR + NJR)
    ITOIA = [0.0] * NIA
    STOIA = [0.0] * NIA

    es_madrugada = (NSR + NJR == 0)

    while T < TF or NS > 0:
        min_TPSO = min(TPSO) if TPSO else HV
        min_TPSIA = min(TPSIA) if TPSIA else HV

        T = min(TPLL, min_TPSO, min_TPSIA)

        if T >= TF and TPLL != HV:
            TPLL = HV

        if T == TPLL and TPLL != HV:
            TPLL = T + fn_generar_ia()
            if TPLL >= TF:
                TPLL = HV

            llamadas_en_espera = NS - NSO - NSIA
            if evaluar_arrepentimiento(llamadas_en_espera):
                ARRE += 1
            else:
                NS += 1

                if NSO < (NSR + NJR):
                    NSO += 1
                    libres = [i for i, t in enumerate(TPSO) if t == HV]
                    idx_elegido = max(libres, key=lambda i: STOO[i])

                    tipo_consulta = obtener_tipo_consulta()
                    t_base = obtener_tiempo_base(tipo_consulta)
                    es_junior = idx_elegido < NJR
                    TA = calcular_tiempo_junior(t_base) if es_junior else t_base

                    TA = TA * (1 - MEJORA_OP[idx_elegido])

                    TPSO[idx_elegido] = T + TA
                    STOO[idx_elegido] += T - ITOO[idx_elegido]

                elif NSIA < NIA:
                    NSIA += 1
                    libres_ia = [i for i, t in enumerate(TPSIA) if t == HV]
                    idx_ia = max(libres_ia, key=lambda i: STOIA[i])

                    tipo_consulta = obtener_tipo_consulta()
                    t_base = obtener_tiempo_base(tipo_consulta)
                    estado, TA = procesar_logica_ia(t_base, es_madrugada)

                    ESTADO_IA[idx_ia] = estado
                    TPSIA[idx_ia] = T + TA
                    STOIA[idx_ia] += T - ITOIA[idx_ia]

        elif T == min_TPSO and TPSO and T != HV:
            idx = TPSO.index(T)
            NS -= 1
            NSO -= 1

            if idx < NJR: NTJr += 1
            else: NTSr += 1

            if NSP > 0 or (NS - NSO - NSIA) > 0:
                if NSP > 0: NSP -= 1
                NSO += 1
                tipo_consulta = obtener_tipo_consulta()
                t_base = obtener_tiempo_base(tipo_consulta)
                es_junior = idx < NJR
                TA = calcular_tiempo_junior(t_base) if es_junior else t_base

                TA = TA * (1 - MEJORA_OP[idx])

                TPSO[idx] = T + TA
            else:
                TPSO[idx] = HV
                ITOO[idx] = T

        elif T == min_TPSIA and TPSIA and T != HV:
            idx_ia = TPSIA.index(T)
            estado = ESTADO_IA[idx_ia]
            NSIA -= 1

            if estado == 'RESUELTO':
                NTIA_A += 1
                NS -= 1
            elif estado == 'FALLO':
                NTIA_F += 1
                NS -= 1
            elif estado == 'DERIVADO':
                NTIA_D += 1
                if NSO < (NSR + NJR):
                    NSO += 1
                    libres = [i for i, t in enumerate(TPSO) if t == HV]
                    idx_op = max(libres, key=lambda i: STOO[i])
                    tipo_consulta = obtener_tipo_consulta()
                    t_base = obtener_tiempo_base(tipo_consulta)
                    TA = calcular_tiempo_junior(t_base) if idx_op < NJR else t_base

                    TA = TA * (1 - MEJORA_OP[idx_op])

                    TPSO[idx_op] = T + TA
                    STOO[idx_op] += T - ITOO[idx_op]
                else:
                    NSP += 1

            if (NS - NSO - NSP - NSIA) > 0:
                NSIA += 1
                tipo_consulta = obtener_tipo_consulta()
                t_base = obtener_tiempo_base(tipo_consulta)
                nuevo_estado, TA = procesar_logica_ia(t_base, es_madrugada)
                ESTADO_IA[idx_ia] = nuevo_estado
                TPSIA[idx_ia] = T + TA
            else:
                TPSIA[idx_ia] = HV
                ITOIA[idx_ia] = T

    for i in range(NSR + NJR):
        if TPSO[i] == HV: STOO[i] += (T - ITOO[i])
    for j in range(NIA):
        if TPSIA[j] == HV: STOIA[j] += (T - ITOIA[j])

    STOSr = sum(STOO[NJR:NJR+NSR]) if NSR > 0 else 0
    STOJr = sum(STOO[0:NJR]) if NJR > 0 else 0
    STOIA_Total = sum(STOIA) if NIA > 0 else 0
    NTIA = NTIA_A + NTIA_D + NTIA_F

    PASr = (NTSr / (NTSr + ARRE) * 100) if (NTSr + ARRE) > 0 else 0
    PTOSr = (STOSr / (T * NSR) * 100) if NSR > 0 else 0
    PAJr = (NTJr / (NTJr + ARRE) * 100) if (NTJr + ARRE) > 0 else 0
    PTOJr = (STOJr / (T * NJR) * 100) if NJR > 0 else 0
    PTOIA_pct = (STOIA_Total / (T * NIA) * 100) if NIA > 0 else 0
    PCR = (NTIA_A / NTIA * 100) if NTIA > 0 else 0
    PCD = (NTIA_D / NTIA * 100) if NTIA > 0 else 0
    PCF = (NTIA_F / NTIA * 100) if NTIA > 0 else 0

    Total_Atendidos = NTSr + NTJr + NTIA_A + NTIA_F
    PA = (Total_Atendidos / (Total_Atendidos + ARRE) * 100) if (Total_Atendidos + ARRE) > 0 else 0

    capacidad_total = NSR + NJR + NIA
    PTO = ((STOSr + STOJr + STOIA_Total) / (T * capacidad_total) * 100) if capacidad_total > 0 else 0

    return {
        "Turno": nombre_turno,
        "SLA (PA)": round(PA, 2),
        "Arrepentidos": ARRE,
        "Total Atendidos": Total_Atendidos,
        "Atendidos Sr": NTSr,
        "Atendidos Jr": NTJr,
        "Atendidos IA (Total)": NTIA,
        "IA Resueltos (%)": round(PCR, 2),
        "IA Derivados (%)": round(PCD, 2),
        "IA Fallos (%)": round(PCF, 2),
        "PTO General (%)": round(PTO, 2),
        "PTO Seniors (%)": round(PTOSr, 2),
        "PTO Juniors (%)": round(PTOJr, 2),
        "PTO IA (%)": round(PTOIA_pct, 2)
    }

